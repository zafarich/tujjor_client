            formData.append("fileContract", this.shopCreate.fileContract);
            formData.append("user", this.$auth.user._id);
            return formData;
        },

        formValidation() {
            const fullName = !!this.shopCreate.fullNameDirector;
        },

        // Search is Shop Created Before if it's OK store shop id for deleting!
        async isHasShop() {
            try {
                await this.$axios
                    .$get("shop/user/me")
                    .then(res => {
                        if (res.success) {
                            this.shopCreate.id = res.data._id;
                            if (!!res.data.status) {
                                this.shopCreate.shopStatus = true;
                                console.log(
                                    "shop status",
                                    this.shopCreate.shopStatus
                                );
                            }
                        }
                    })
                    .catch(err => console.error(err));
            } catch (err) {
                console.log(err);
            }
        },

        // Delete shop before update data of shop
        async deleteShop() {
            try {
                await this.$axios
                    .$delete("shop/" + this.shopCreate.id)
                    .then(res => {
                        if (res.success) {
                            this.shopCreate.id = "";
                        }
                    })
                    .catch(err => console.error(err));
            } catch (err) {
                console.log(err);
            }
        },

        // HTTP request for Creating Shop
        async sendCreateShop() {
            this.loadSpinner = true;
            this.shopCreate.phone = this.shopCreate.phone.replace(
                /[^0-9]/g,
                ""
            );
            const formData = this.createFormData();
            console.log(formData);

            try {
                await this.isHasShop();
                if (!!this.shopCreate.id) await this.deleteShop();
                await this.$axios
                    .$post("shop/create", formData, {
                        headers: {
                            "Access-Control-Allow-Origin": "*",
                            "Access-Control-Allow-Methods":
                                "GET, POST, PATCH, PUT, DELETE, OPTIONS",
                            "Access-Control-Allow-Headers":
                                "Origin, Content-Type, X-Auth-Token"
                        }
                    })
                    .then(response => {
                        if (response.success) {
                            this.loadSpinner = false;
                            this.shopCreate.sendReqModal = true;
                        } else {
                            throw new Error("Could not save data!");
                        }
                    })
                    .catch(error => {
                        this.loadSpinner = false;
                        // handle error
                        console.log(error);
                    });
            } catch (err) {
                console.log(err);
            }
            console.log("end");
        }
    },

