                            if (!!this.shopCreate.id)
                                this.shopCreate.modalShow = true;
                        }
                    } else {
                        throw new Error("Could not save data!");
                    }
                })
                .catch(error => {
                    // handle error
                    console.log(error);
                });
        } catch (err) {
            console.log(err);
        }

        this.isGet = true;
    }
